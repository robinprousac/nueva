package com.example.nueva.library;

public class Book_description {

    String cover_url;
    String tipo;
    String correlativo;
    String campo;
    String descripcion_campo;
    String subcampo;
    String valor;

    public String getCover_url() {
        return cover_url;
    }

    public void setCover_url(String cover_url) {
        this.cover_url = cover_url;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getCorrelativo() {
        return correlativo;
    }

    public void setCorrelativo(String correlativo) {
        this.correlativo = correlativo;
    }

    public String getCampo() {
        return campo;
    }

    public void setCampo(String campo) {
        this.campo = campo;
    }

    public String getDescripcion_campo() {
        return descripcion_campo;
    }

    public void setDescripcion_campo(String descripcion_campo) {
        this.descripcion_campo = descripcion_campo;
    }

    public String getSubcampo() {
        return subcampo;
    }

    public void setSubcampo(String subcampo) {
        this.subcampo = subcampo;
    }

    public String getValor() {
        return valor;
    }

    public void setValor(String valor) {
        this.valor = valor;
    }
}
